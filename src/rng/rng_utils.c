#include "maths2_rng.h"

uint64_t	rng_splitmix64(uint64_t *seed)
{
	uint64_t	z;

	*seed += 0x9E3779B97F4A7C15ULL;
	z = *seed;
	z = (z ^ (z >> 30)) * 0xBF58476D1CE4E5B9ULL;
	z = (z ^ (z >> 27)) * 0x94D049BB133111EBULL;
	return (z ^ (z >> 31));
}
